package org.example.model;

public class Shoot {
    private long x;

    private long y;

    public Shoot() {

    }

    public Shoot(long x, long y) {
        this.x = x;
        this.y = y;
    }

    public long getX() {
        return x;
    }

    public void setX(long x) {
        this.x = x;
    }

    public long getY() {
        return y;
    }

    public void setY(long y) {
        this.y = y;
    }
}
